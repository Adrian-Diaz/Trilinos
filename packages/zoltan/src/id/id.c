int LB_compare_combo_id(ID *id1, ID *id2) 
{
/*
 *  This function compares IDs made up of two integer fields.
 *  It returns -1 if id1 < i2; 1 if id1 > id2; and 0 if id1 == id2.
 */

  if ((*id1).Proc < (*id2).Proc) 
    return -1;
  else if ((*id1).Proc > (*id2).Proc)
    return  1;
  else if ((*id1).Number < (*id2).Number)
    return -1;
  else if ((*id1).Number > (*id2).Number)
    return 1;
  else
    return 0;

}

